package com.sist.main;

class BlockTest {
	static {
		System.out.println("static { }");//클래스 초기화블럭 가장 먼저 실행
	}
	{
		System.out.println("{ }");//인스턴스 초기화 블럭
	}

	public BlockTest() {
		System.out.println("생성자");
	}

	public static void main(String[] args) {
		System.out.println("BlockTest bt = new BlockTest();");//메인 메서드 두번째로 실행
		BlockTest bt = new BlockTest();//BlockTest 인스턴스 생성

		System.out.println("BlockTest bt2 = new BlockTest();");
		BlockTest bt2 = new BlockTest();
	}
}
