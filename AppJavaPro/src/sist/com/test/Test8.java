package sist.com.test;

import java.util.Scanner;

public class Test8 {

	
	
	public static void main(String[] args) {
		int menu=0;
		int num=0;
		
		Scanner scanner =new Scanner(System.in);
		
		
		while(true) {
			System.out.println("(1)햄버거");
			System.out.println("(2)피자");
			System.out.println("(3)샌드위치");
			System.out.print("원하는 메뉴(1~3)를 선택하세요.(종료:0)");
			
			String tmp= scanner.nextLine();//화면에서 입력받은 내용을 tmp에 저장
			menu=Integer.parseInt(tmp);//입력받은 문자열(tmp)를 숫자로 변환
			
			if(menu==0) {
				System.out.println("프로그램을 종료합니다.");
			break;
			}else if(menu==1) {
				System.out.println("햄버거를 선택하셨습니다. 감사합니다.");
			}else if(menu==2) {
				System.out.println("피자를 선택하셨습니다. 감사합니다.");
				break;
			}else if(menu==3) {
				System.out.println("샌드위치를 선택하셨습니다. 감사합니다.");
				break;
			}else if(!(1<=menu&&menu<=3)) {
				System.out.println("메뉴를 잘못 선택하셨습니다.(종료는 0");
				continue;
			}

		}
	}
}
